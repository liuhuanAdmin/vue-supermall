<template>
  <swiper>
    <swiper-item v-for="item in banners">
      <a :href="item.link">
        <img :src="item.image" alt="" @load="imageLoad">
      </a>
    </swiper-item>
  </swiper>
</template>

<script>
import {Swiper,SwiperItem} from '@/components/common/swiper'

export default {
name: "homesSwiper",
  components: {
    Swiper,
    SwiperItem
  },
  props: {
    banners: {
      type: Array,
      default() {
        return []
      }
    },
  },
  data(){
  return {
    imgload: false
  }
  },
  methods: {
    imageLoad() {
      if (!this.imgload){
        this.$emit('imageLoad')
        this.imgload = true}
    }
  }
}
</script>

